import { z } from "zod";
import { otpSchema } from "../validation/index.js";

// ! ZOD SCHEMA TO VALIDATE THE REQUEST BODY FOR VERIFYING AN OTP (ONE-TIME PASSWORD). THIS SCHEMA ENSURES THAT THE REQUEST BODY CONTAINS AN "otp" FIELD THAT ADHERES TO THE DEFINED otpSchema, AND IT USES .strict() TO DISALLOW ANY ADDITIONAL FIELDS IN THE REQUEST BODY. THIS VALIDATION SCHEMA IS USED IN A MIDDLEWARE TO VALIDATE INCOMING REQUESTS TO THE OTP VERIFICATION ENDPOINT, ENSURING THAT ONLY VALID DATA IS PROCESSED BY THE CONTROLLER LOGIC.
const verifyOtpSchema = {
  body: z
    .object({
      otp: otpSchema,
    })
    .strict(),
};

export default verifyOtpSchema;
